


function addNumbers(n1, n2){
    return n1 + n2;
}

function subNumbers(n1, n2){
    return n1 - n2;
}

function multiplyNumbers(n1, n2){
    return n1 * n2;
}

function divideNumbers(n1, n2){
    return n1 / n2;
}

export {
    addNumbers,
    subNumbers,
    multiplyNumbers,
    divideNumbers
};